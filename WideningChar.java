class WideningChar
 {
    public static void main(String[]args)
     { 
       char ch=100;
       System.out.println(ch);

     
       int i1=ch;
       System.out.println(i1);

        long l1=ch;
        System.out.println(l1);

       float f1=ch;
       System.out.println(f1);

       double d1=ch;
       System.out.println(d1);

     }

}