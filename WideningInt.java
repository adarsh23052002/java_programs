class WideningInt
 {
    public static void main(String[]args)
     {
              
        int i1=500;
       System.out.println(i1);

      

 
       long l1=i1;
       System.out.println(l1);

       float f1=i1;
       System.out.println(f1);

       double d1=i1;
       System.out.println(d1);



     }

}