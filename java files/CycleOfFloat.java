class CycleOfFloat
{
        public static void main(String[]args)
        {
         float b1=3.400000000000000000000f;
         System.out.println(b1);


        float b2= (float)3.4000000000000000000000002f;
        System.out.println(b2);


         float b3= (float)4.0000000f;
         System.out.println(b3);

        
        }
}